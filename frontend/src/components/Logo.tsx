interface LogoProps {
  className?: string;
  size?: "sm" | "md" | "lg";
}

export default function Logo({ className = "", size = "md" }: LogoProps) {
  const sizeClasses = {
    sm: "w-5 h-5",
    md: "w-8 h-8",
    lg: "w-12 h-12",
  };

  return (
    <img
      src="/favicon.svg"
      alt="Deliverty"
      className={`${sizeClasses[size]} ${className}`}
    />
  );
}

